    def check_default_images(self):
        """–ü—Ä–æ–≤–µ—Ä–∏—Ç—å –Ω–∞–ª–∏—á–∏–µ –ø–∞–ø–∫–∏ images —Ä—è–¥–æ–º —Å–æ —Å–∫—Ä–∏–ø—Ç–æ–º"""
        if os.path.exists("images"):
            self.image_folder = "images"
            self.folder_label.config(text="images (–∞–≤—Ç–æ)")
            self.load_images()
    
    def load_images(self):
        """–ó–∞–≥—Ä—É–∑–∏—Ç—å –≤—Å–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏–∑ –ø–∞–ø–∫–∏"""
        if not self.image_folder:
            return
        
        self.recipe_images = {}
        
        for filename in os.listdir(self.image_folder):
            if filename.lower().endswith(('.png', '.jpg', '.jpeg', '.gif', '.bmp')):
                try:
                    path = os.path.join(self.image_folder, filename)
                    image = Image.open(path)
                    image = image.resize((150, 100), Image.Resampling.LANCZOS)
                    photo = ImageTk.PhotoImage(image)
                    self.recipe_images[filename] = photo
                    
                    # –û–±–Ω–æ–≤–ª—è–µ–º –º–∏–Ω–∏–∞—Ç—é—Ä—ã –Ω–∞ –∫–Ω–æ–ø–∫–∞—Ö
                    for btn in self.recipe_buttons:
                        recipe = RECIPES.get(btn.cget("text"))
                        if recipe and recipe.get("image") == filename:
                            btn.image_label.config(image=photo, text="")
                            btn.image_label.image = photo  # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Å—Å—ã–ª–∫—É
                    
                except Exception as e:
                    print(f"–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ {filename}: {e}")
    
    def show_recipe(self, recipe_name):
        """–ü–æ–∫–∞–∑–∞—Ç—å –≤—ã–±—Ä–∞–Ω–Ω—ã–π —Ä–µ—Ü–µ–ø—Ç"""
        recipe = RECIPES.get(recipe_name)
        
        if not recipe:
            messagebox.showerror("–û—à–∏–±–∫–∞", "–†–µ—Ü–µ–ø—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω!")
