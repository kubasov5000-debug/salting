    def save_recipe(self):
        """–°–æ—Ö—Ä–∞–Ω–∏—Ç—å —Ç–µ–∫—É—â–∏–π —Ä–µ—Ü–µ–ø—Ç –≤ —Ñ–∞–π–ª"""
        recipe_name = self.recipe_title.cget("text")
        if not recipe_name:
            messagebox.showwarning("–í–Ω–∏–º–∞–Ω–∏–µ", "–ù–µ—Ç –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ —Ä–µ—Ü–µ–ø—Ç–∞ –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è")
            return
        
        recipe = RECIPES.get(recipe_name)
        if not recipe:
            return
        
        filename = filedialog.asksaveasfilename(
            defaultextension=".txt",
            filetypes=[("–¢–µ–∫—Å—Ç–æ–≤—ã–µ —Ñ–∞–π–ª—ã", "*.txt"), ("–í—Å–µ —Ñ–∞–π–ª—ã", "*.*")],
            initialfile=f"–†–µ—Ü–µ–ø—Ç_{recipe_name}.txt"
        )
        
        if filename:
            try:
                with open(filename, 'w', encoding='utf-8') as f:
                    f.write(f"–†–ï–¶–ï–ü–¢: {recipe_name}\n")
                    f.write("="*50 + "\n\n")
                    f.write(f"–í—Ä–µ–º—è –ø—Ä–∏–≥–æ—Ç–æ–≤–ª–µ–Ω–∏—è: {recipe.get('time', '–ù–µ —É–∫–∞–∑–∞–Ω–æ')}\n")
                    f.write(f"–°–ª–æ–∂–Ω–æ—Å—Ç—å: {recipe.get('difficulty', '–ù–µ —É–∫–∞–∑–∞–Ω–æ')}\n\n")
                    f.write("–ò–ù–ì–†–ï–î–ò–ï–ù–¢–´:\n")
                    f.write("-"*30 + "\n")
                    for ingredient in recipe["ingredients"]:
                        f.write(f"‚Ä¢ {ingredient}\n")
                    f.write("\n–°–ü–û–°–û–ë –ü–†–ò–ì–û–¢–û–í–õ–ï–ù–ò–Ø:\n")
                    f.write("-"*30 + "\n")
                    f.write(recipe["steps"] + "\n\n")
                    f.write("="*50 + "\n")
                    f.write("–ü—Ä–∏—è—Ç–Ω–æ–≥–æ –∞–ø–ø–µ—Ç–∏—Ç–∞! üçΩÔ∏è\n")
                
                self.status.config(text=f"–†–µ—Ü–µ–ø—Ç —Å–æ—Ö—Ä–∞–Ω–µ–Ω –≤: {filename}")
                messagebox.showinfo("–£—Å–ø–µ—Ö", f"–†–µ—Ü–µ–ø—Ç —Å–æ—Ö—Ä–∞–Ω–µ–Ω –≤ —Ñ–∞–π–ª:\n{filename}")
                
            except Exception as e:
                messagebox.showerror("–û—à–∏–±–∫–∞", f"–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å —Ñ–∞–π–ª:\n{e}")
    
