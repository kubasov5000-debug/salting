    def add_ingredient_to_search(self, ingredient):
        """–î–æ–±–∞–≤–∏—Ç—å –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç –≤ –ø–æ–ª–µ –ø–æ–∏—Å–∫–∞"""
        current_text = self.search_text.get(1.0, tk.END).strip()
        if current_text:
            new_text = f"{current_text}, {ingredient}"
        else:
            new_text = ingredient
        
        self.search_text.delete(1.0, tk.END)
        self.search_text.insert(1.0, new_text)
    
